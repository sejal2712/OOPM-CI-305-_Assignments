//“Write a Program for use of predefined sizeof() function to display the data type”

#include<iostream>
using namespace std;

int main()
{
cout<<"the size of integer data type is  "<< sizeof(int)<<"  bytes" <<endl ;  
cout<<"the size of character data type is  "<< sizeof(char)<<"  bytes"<<endl  ;  
cout<<"the size of float data type is  "<< sizeof(float)<<"  bytes" <<endl ;  
     
 return 0;   
}
